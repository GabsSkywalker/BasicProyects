print("""================================
convertidor de numeros a letras
================================""")

numero = int(input("Que numero desea convertir: "))



if 0 == numero :
    print("El numero se escribe Cero")

elif 1 == numero :
    print("El numero se escribe uno")

elif 2 == numero :
    print("El numero se escribe dos")

elif 3 == numero :
    print("El numero se escribe tres")

elif 4 == numero :
    print("El numero se escribe cuatro")

elif 5 == numero :
    print("El numero se escribe 5")

else :
    print("este programa solo convierte hasta el numero 5")

